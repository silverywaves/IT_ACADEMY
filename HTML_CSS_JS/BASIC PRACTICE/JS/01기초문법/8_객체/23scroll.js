/**
 *  window.scrollTo(x좌표, y좌표);      - 스크롤 위치를 특정 좌표로 이동
 *  window.scrollBy(x좌표, y좌표);      - 스크롤 위치를 현재 위치에서 상대적인 위치로 이동
 */

window.scrollTo(100, 200);
    // 몇 번을 호출하더라도 가로는 100px, 세로는 200px의 위치로 스크롤 이동

window.scrollBy(100, 200);
    // 호출할때마다 웹 브라우저 스크롤이 가로방향으로 100px, 세로방향으로 200px씩 계속 이동