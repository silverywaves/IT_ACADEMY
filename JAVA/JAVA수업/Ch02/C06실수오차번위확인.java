package Ch02;

public class C06실수오차번위확인 {

	public static void main(String[] args) {
		// 실수는 오차가 생기니까 정수로 변환해서 사용
		System.out.println("Float : "+(0.555555555555555F));
		System.out.println("Double : "+(0.5555555555555555555));
		System.out.println("Float : "+(0.999999999999999999999999F));
		System.out.println("Float : "+(0.222222222222222222222F));
		System.out.println("Float : "+(0.444444444444444444444F));
		System.out.println("Double : "+(0.999999999999999999999999));
		System.out.println("Double : "+(0.222222222222222222222));
		System.out.println("Double : "+(0.444444444444444444444));
	}

}
