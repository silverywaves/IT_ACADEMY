// 단항 산술 연산자 : 피연산자 1개 필요
// ++ & -- 두 연산자를 합쳐 증감연산자라고도 함

let increment = 10;
increment++;        // 값을 1 증가시킴

let decrement = 10;
decrement--;        // 값을 1 감소시킴

// let increment = 10++;
// 숫자에 바로 사용불가. 변수나 상수에 할당된 데이터로만 연산가능

console.log(increment);     // 11
console.log(decrement);     // 9