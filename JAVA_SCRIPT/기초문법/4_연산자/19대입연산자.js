// 대입 연산자 : 데이터를 대입(할당)하는 연산을 수행하는 연산자
// 복합 대입 연산자 : 산술 연산자와 대입 연산자를 함께 사용하여 산술과 할당을 한번에 수행하는 연산자

let x = 10;
x += 5;     // 15(x = x + 5)

let y = 10;
y -= 5;     // 5(y = y - 5)

let z = 10;
z *= 5;      // 50(z = z * 5)

let u = 10;
u /= 5;     // 2 (u = u / 5)

let v = 10;
v %= 3;     // 1 (v = v % 3) 

let t = 10;
t **= 2     // 100 (t = t ** 2)

console.log(x);
console.log(y);
console.log(z);
console.log(u);
console.log(v);
console.log(t);